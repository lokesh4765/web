               Mindrolling or Place of Perfect Emancipation is the largest Buddhist learning center of India is devoted to the research of Buddhist scriptures, Tibetan lunar calendar, astronomy, traditional Tibetan medicine and calligraphy.

The 220 feet high Buddhist temple tops the list of most popular places to visit in Dehradun. Key attractions here are – the spectacular holy shrine of Buddha and Guru Padmasambhava and vibrant looking wall paintings, depicting the life and works of Lord Buddha. The monastery complex has a well maintained manicured garden and a buzzing shopping arcade selling artifacts, souvenirs and knick knacks.

But what totally steals the show here is the spectacle of nature! Top floors of the building offer jaw dropping views of Doon Valley that you shouldn’t miss for anything.
