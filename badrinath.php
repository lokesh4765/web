    <center><h2> Best Season / Best time to visit</h2></center> 
    <hr>
    Badrinath has a cool and chilly climate all through the year but the best time to visit is from May to June and again from September to October.
    <br><br>
    <b>Winter (October to April)</b> has chilly days with average minimum touching near 5°C. Minimum can touch sub zero levels and snowfall is very common. These months are not the perfect time for travelling.
      <br><br>
      <b>Summer (May to June)</b> are very pleasant with moderately cold climate, with average around 18°C. Summers is ideal for all sight seeing and the holy Badrinath pilgrimage.
     <br><br>
     <b>Monsoon (July to mid-September)</b> are accompanied with regular rains and also temperature drops down to 15°C. The region is prone to occasional landslides and travelling can be difficult.
     <br><br>
      The holy city of Badrinath is open for public views from May to October but the temple remains closed during the monsoon months as landslides are common and can mar the journey of the pilgrims. The region experiences pleasant and cool summer while winters are very chilly and snowfall is a regular occurrence.
