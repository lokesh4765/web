    <h3>Major attractions of Badrinath</h3>
    <hr>
    <ul>
        <li><b>TaptKund:</b> Tapt Kund, natural spring, with water which is believed to possess curing properties.</li><br>
         <li><b>Brahma Kapal: </b>Tourists visit this site to perform ceremonies for deceased relatives.</li><br>
          <li><b>Neelkanth:</b> The peak is named after Lord Shiva and receives the first rays of sunlight every day.</li><br>
          <li><b>Mata Murti Temple:</b> Mata Murti Temple is a worshipping place for Lord Narayan.</li><br>
         <li><b>Charanpaduka:</b> In a stunning meadow, surrounded by flowers, lies a huge boulder with Lord Vishnu’s footprints.</li><br>
            <li><b>Narad Kund: </b> It is believed that this popular Kund recovered the famous idol of Badrinath.</li><br>
           <li><b>Mana: </b>The village area nearby Badrinath temple called as Mana is a stunning area and surrounded by religious tourists from all over the world.</li><br>
           <li><b>Bheem Pul:</b> For an adventurous time flushed with religious significance, Bheem Pul near Badrinath Temple is ideal.</li><br>
            <li><b>Vyas Cave:</b> This is the cave where Ved Vyas used to meditate and told his Mahahbharata to God Ganesha.</li><br>
           <li><b>Ganesha Cave:</b> The cave which is built naturally is believed to be the site where Ved Vyas wrote Mahabharata.</li><br>
           <li><b> Sheshnetre: </b>On the opposite bank of the river Alaknanda, in the lap of Nar Parvat, there are two small seasonal lakes. Between these lakes is a boulder having an impression of the legendary snake, Sheshnag. The formation of eye on the boulder is natural.</li> <br>
           <li><b>Panch Shilas:</b> The Panch Dharas are famous in Badaripuri are Bhrigu, Prahlad, Kurma, Indira dhara and Urvashi. Most exciting feature of the Shilas is Indira dhara which is located 1.5 km North of Badaripuri town.</li><br>
    </ul>
