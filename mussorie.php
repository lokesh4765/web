    <h3><u> PLACES TO VISIT IN MUSSORIE</u></h3>
        <p>
        <fieldset>
            <legend><big> Kempty Falls: For the splashes and chills</big> </legend>
            <p>
                <img src="photos/Kempty-Falls.jpg" hight="30%" width="30%" align="left">
                Surrounded by majestic mountain ranges, the Kempty Falls is one of the major tourist attractions in Mussoorie. It is situated at 1,364 m and allows the visitors to savor the majestic views of the hill station and natural splendors all around. The melodious sound of the waterfall can be heard from a distance and after reaching there, one is simply left spellbound seeing the serene landscapes.

                <b><h3>Things to do:</h3></b> Splash in the cool, refreshing water and have a picnic with your near and dear ones

             <b><h3>Opening hours: </h3></b>6 am to 6 pm 

             <b><h3>Entry fee:</h3></b> Free
            </p>
        </fieldset><br>
        <fieldset>
            <legend><big> Jharipani Falls: For splendid serenity amidst roaring waters </big> </legend>
            <p>
                <img src="photos/Jharipani-Falls.jpg" hight="30%" width="30%" align="left">
                If you are a nature lover, then you must visit the famous Jharipani Falls, located close to Mussoorie. Trekkers love the area around the waterfalls as the scenery is amazing. Picnics with loved ones can be planned while enjoying the peace and serenity of this place. The beautiful sights are worth capturing.

                <b><h3>  Things to do:</h3></b> Trekking and enjoy a small little picnic here

                <b><h3>Opening hours: </h3></b>6 am to 6 pm

                <b><h3>Entry fee:</h3></b> Free
            </p>
        </fieldset><br>
   <fieldset>
            <legend><big>Mossy Falls: For the music of a different kind </big> </legend>
            <p>
                <img src="photos/mossy-fall.jpg" hight="30%" width="30%" align="left">
                Located in a dense forest, Mossy Falls is also among the places to visit in Mussoorie for the nature lovers. It is just 7 km away from the hill station on the Bala Hisar route. The scenic views and the peaceful surroundings will make you fall in love with a spot.

                <b><h3>Things to do:</h3></b> Soak in the scenic views and melodious gush of the waterfall

<b><h3>Opening hours: </h3></b>6 am to 6 pm

<b><h3>Entry fee:</h3></b> Free
            </p>
        </fieldset><br>
   <fieldset>
            <legend><big> Mussoorie Lake: The man-made lake that doesn’t look like one</big> </legend>
            <p>
                <img src="photos/Mussoorie-lake.jpg" hight="30%" width="30%" align="left">
                Tourists, as well as locals, love to visit this beautiful picnic spot where there is a lot to enjoy for the children too. This is an artificial lake but has been beautifully developed by the authorities as a lovely tourist spot. People can enjoy boating in the lake and enjoy mesmerizing scenic views. The lake area also has numerous eateries that offer good food.

<b><h3>Things to do: </h3></b>Go boating, gorge on finger licking fast food, enjoy scenic views, and click pictures

<b><h3>Opening hours:</h3></b> 8 am to 7 pm

<b><h3>Entry fee:</h3></b> INR 15
            </p>
        </fieldset><br>
   <fieldset>
            <legend><big> Camel’s Back Road: For that walk of a different kind </big> </legend>
            <p>
                <img src="photos/camel-back-road.jpg" hight="30%" width="30%" align="left">
                Well, this is about that walk to remember. The rock formation at the end of the road is in the shape of a camel’s hunched back and hence the name. This road is almost 3 km long. Starting from the Liberty Point and ending at the Kulri Bazaar, this road stays a little crowded in summers and attracts tourists from all over. It offers one of the most amazing views of the Doon valley and has a lot of small eateries that serve mouthwatering snacks.

                <b><h3>Things to do:</h3></b> Enjoy Doon Valley’s views, hot snacks in the evening chill, and horse riding

<b><h3>Opening hours: </h3></b>Always open
            </p>
        </fieldset><br>
        <fieldset>
            <legend><big>Gun Hill: A scenic beauty from the top </big> </legend>
            <p>
                <img src="photos/gun-hill.jpg" hight="30%" width="30%" align="left">
                The cable car ride in Mussoorie is another major attraction that draws a lot of tourists. It’s a 10 minutes’ ride that starts from Jhula Ghar to Gun Hill. One can enjoy amazing views of the mountains and meadows of Doon Valley up the hill on the ropeway. The sunset views from the cars are especially stunning while riding the cable car.

                <b><h3>Things to do:</h3></b> Enjoy sunset views, savor hot snacks and tea, and play a lot of interesting games

<b><h3>Opening hours: </h3></b>10 am to 6 pm

<b><h3>Entry fee: </h3></b>INR 60
            </p>
        </fieldset><br>
        <fieldset>
            <legend><big>K Dev Bhoomi Wax Museum: The Indian Madame Tussauds </big> </legend>
            <p>
                <img src="photos/K-Devbhoomi-Wax-Museum.jpg" hight="30%" width="30%" align="left">
                The Indian version of Tussauds, this wax museum located in the Company Garden is among the famous tourist places in Mussoorie. The museum houses wax statues of some of the most renowned nationals as well as international celebrities including Leonardo diCaprio and Angelina Jolie.

                <b><h3>Major attraction:</h3></b> Wax statues of the famous celebrities, souvenirs

<b><h3>Opening hours: </h3></b>9 am to 6 pm

<b><h3>Entry fee:</h3></b> INR 500
            </p>
        </fieldset><br>
        <fieldset>
