        “The world keeps on changing, but there is always something, somewhere, that remains the same.” – Ruskin Bond

        Landour, which was a Cantonment town in British India was named after a village called Llanddowror in south west Wales. Lost in the mists of Himalayas, draped in scarlet red colour of rhododendrons & thick deodars, Landour reminds you of the British Raj. A 5-kilometer steep drive or walk from the crowded bazaars of Mussoorie, it’s a different world of peace and lush views.

        Landour should be on every mountain-lover’s bucket list, but if you are interested in a typical things-to-see or places-to-go type of list, this isn’t the place for you. Here’s your pocket guide to Landour for a refreshing trip. 
        <br>
    <h1>Away from the noise, into the calm</h1>
    <CENTER><img src="photos/Landour.jpg" hight="50%" width="50%"></center>
    If you love walking, this is going to be one of the most beautiful and calm mountain walks of your life. Through the misty Deodar forest on the winding path, where the air is filled with woody smell and flowers are burgeoning on the hillside, a casual walk in Landour is a surreal experience for you.

    <b><h3>How to reach:</h3></b> One can walk from Mussoorie bazaar to Char Dukan area (in Landour), which takes only about 40 minutes. You can also hire a cab from Mussoorie bazaar for INR 200-300, which I don’t recommended.

    <b><h3>Best time to visit Landour:</h3></b> Landour is pleasant and peaceful throughout the year. April to June is the best time to visit Landour due to its comfortable weather. August and September are rainy months, but also one of the most beautiful months to spend in Landour.
    <center><h1><u>Exploring the town & its little secrets</u></h1></center>
Though there’s nothing much to explore in this little sleepy town, there certainly are some points you wouldn’t like to miss while you’re here. And this travel guide to Landour is all about those little secrets.
